/** Detect the active locale from browser/user preference. Defaults to "pt". */
export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "pt";
  const lang = navigator.language?.toLowerCase() ?? "";
  if (lang.startsWith("en")) return "en";
  if (lang.startsWith("es")) return "es";
  if (lang.startsWith("ja")) return "ja";
  if (lang.startsWith("fr")) return "fr";
  return "pt";
}

/** Build a translation function for the given locale. */
export function buildTranslator(locale: Locale) {
  const dict = (translations[locale] ?? translations.en) as Record<string, string>;
  return function t(key: TranslationKey, vars?: Record<string, string>): string {
    let str = dict[key] ?? (translations.en as Record<string, string>)[key] ?? key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(`{${k}}`, v);
      }
    }
    return str;
  };
}
